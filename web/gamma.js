	this.den = 0.9*(1 - this.bl);
	this.p0 = (Math.pow(10,-this.wp*this.mul) - this.bl)/this.den;
	this.d0 = (((Math.pow(10,((0.0001*this.cv)-this.wp)*this.mul) - this.bl)/this.den)-this.p0)/0.0001;
